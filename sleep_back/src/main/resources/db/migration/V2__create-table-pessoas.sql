CREATE TABLE PESSOAS (
    ID BIGINT PRIMARY KEY AUTO_INCREMENT,
    NOME VARCHAR(100),
    CPF VARCHAR(14),
    TELEFONE VARCHAR(20),
    EMAIL VARCHAR(100),
    GENERO VARCHAR(50),
    DATA_NASCIMENTO DATE,
    ID_ENDERECO_ENTREGA BIGINT,
    ID_ENDERECO_COBRANCA BIGINT,
    ATIVO TINYINT,

    CONSTRAINT FK_ENDERECO_ENTREGA FOREIGN KEY (ID_ENDERECO_ENTREGA) REFERENCES ENDERECOS(ID),
    CONSTRAINT FK_ENDERECO_COBRANCA FOREIGN KEY (ID_ENDERECO_COBRANCA) REFERENCES ENDERECOS(ID)
);