CREATE TABLE PEDIDOS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    ID_CLIENTE BIGINT NOT NULL,
    ID_ENDERECO_ENTREGA BIGINT NOT NULL,
    ID_VENDEDOR BIGINT NOT NULL,
    DATA_EMISSAO DATETIME NOT NULL,
    DATA_PREVISAO_ENTREGA DATE,
    VALOR_FRETE DECIMAL(10,2),
    VALOR_MERCADORIA DECIMAL(10,2),
    VALOR_TOTAL DECIMAL(10,2),
    RESPONSAVEL_ENTREGA VARCHAR(100),
    STATUS_PEDIDO VARCHAR(30),
    OBSERVACAO_ADICIONAIS TEXT,
    OBSERVACAO_INTERNA TEXT,
    PRIMARY KEY (ID),
    CONSTRAINT FK_PEDIDO_CLIENTE FOREIGN KEY (ID_CLIENTE) REFERENCES PESSOAS(ID),
    CONSTRAINT FK_PEDIDO_VENDEDOR FOREIGN KEY (ID_VENDEDOR) REFERENCES PESSOAS(ID),
    CONSTRAINT FK_PEDIDO_ENDERECO_ENTREGA FOREIGN KEY (ID_ENDERECO_ENTREGA) REFERENCES ENDERECOS(ID)
);
